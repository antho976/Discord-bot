export function detectTopics(text) {
  const lower = text.toLowerCase();
  const words = _splitWords(lower);
  const scores = {};
  for (const [topic, data] of _COMPILED_TOPICS) {
    let score = 0;
    const matched = [];
    // Check short keywords via word Set
    for (const kw of data.shortKeywords) {
      if (words.has(kw)) { score += data.weight; matched.push(kw); }
    }
    // Check long keywords via includes
    for (const kw of data.longKeywords) {
      if (lower.includes(kw)) { score += data.weight; matched.push(kw); }
    }
    if (score > 0) scores[topic] = { score, matched, confidence: matched.length >= 2 ? 'high' : 'low' };
  }
  const sorted = Object.entries(scores).sort(([, a], [, b]) => b.score - a.score);
  return sorted.length > 0 ? sorted : null;
}

// Pre-compiled topic data — avoids regex compilation and Object.entries() per call
const _splitWords = (text) => new Set(text.split(/[\s,.:;!?()]+/).filter(Boolean));
const _COMPILED_TOPICS = Object.entries(TOPICS).map(([topic, data]) => {
  const shortKeywords = [];
  const longKeywords = [];
  for (const kw of data.keywords) {
    if (kw.length <= 4 && !kw.includes(' ')) shortKeywords.push(kw);
    else longKeywords.push(kw);
  }
  return [topic, { weight: data.weight, shortKeywords, longKeywords }];
});
